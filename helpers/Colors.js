export function PrimaryColor(opacity) {
	return 'rgba(16, 107, 163, ' + String(opacity)  + ')'
}

export function Black(opacity) {
	return 'rgba(0, 0, 0, ' + String(opacity)  + ')'
}

export function White(opacity) {
	return 'rgba(255, 255, 255, ' + String(opacity)  + ')'
}

export function Red(opacity) {
	return 'rgba(244, 95, 65, ' + String(opacity)  + ')'
}

export function Green(opacity) {
	return 'rgba(122, 244, 66, ' + String(opacity)  + ')'
}